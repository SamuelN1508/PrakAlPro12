# Data
List = [1,2,3,4,5]
Set = {1,2,3,4,5}
Tuple = (1,2,3,4,5)

# Mengubah data menjadi tipe data yang diinginkan menggunakan set(), list() atau tuple()
listset = set(List)
setlist = list(Set)
tupset = set(Tuple)
settup = tuple(Set)

# Print semuanya
print("List :", List)
print("List => Set:", listset)
print()

print("Set :", Set)
print("Set => List:", setlist)
print()

print("Tuple :", Tuple)
print("Tuple => Set:", tupset)
print()

print("Set :", Set)
print("Set => Tuple:", settup)